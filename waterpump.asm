MOV P2,#00H
MOV P0,#0FFH
MOV A,#00H

//Full
L1:ACALL WTRCK
	MOV A,P2
	CJNE A,#0F1H,L2 //11110001
	SETB P0.0
	CLR P0.1
	CLR P0.2
	CLR P0.3
	CLR P0.4
	CLR P0.5


//Three-fourth
L2:MOV A,P2
	CJNE A,#0E1H,L3 //11100001
	CLR P0.0
	SETB P0.1
	CLR P0.2
	CLR P0.3
	CLR P0.4
	SETB P0.5


//Half
L3:MOV A,P2
	CJNE A,#0C1H,L4 //11000001
	CLR P0.0
	CLR P0.1
	SETB P0.2
	CLR P0.3
	CLR P0.4
	SETB P0.5


//Quarter
L4:MOV A,P2
	CJNE A,#81H,L5 //10000001
	CLR P0.0
	CLR P0.1
	CLR P0.2
	SETB P0.3
	CLR P0.4
	SETB P0.5


//Empty
L5:MOV A,P2
	CJNE A,#01H,L1 //00000001
	CLR P0.0
	CLR P0.1
	CLR P0.2
	CLR P0.3
	SETB P0.4
	SETB P0.5
	SJMP L1

WTRCK:JNB P2.0,L6
	CLR P0.7
	SJMP L7
	
L6:CLR P0.5
	SETB P0.7

L7:RET

END